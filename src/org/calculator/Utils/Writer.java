package org.calculator.Utils;


/**
 * Класс содержит метод вывода в консоль.
 */
public class Writer {

    /**
     * Метод принимат на вход строку и выводит ее в консоль
     * @param line - входящий параметр типа String
     */
    public void writeLine (String line) {
        System.out.println(line);
    }
}
